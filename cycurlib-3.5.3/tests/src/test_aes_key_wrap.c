static Esc_ERROR
EscTstAesKeyWrap_ComplianceTest(void)
{
    Esc_UINT32 i;
    Esc_UINT32 numTestCases = (Esc_UINT32)(sizeof(EscTstAesKeyWrap_testCases) / sizeof(EscTstAesKeyWrap_testCases[0]));
    Esc_ERROR returnCode = Esc_NO_ERROR;
    Esc_BOOL isEqual;

    Esc_UINT8 result[520];

    EscTst_PrintString( "Compliance test\n" );

    for (i=0; (i < numTestCases) && (returnCode == Esc_NO_ERROR); ++i)
    {
        const EscTstAesKeyWrap_TestCase *tc = &EscTstAesKeyWrap_testCases[i];

        Esc_ASSERT(sizeof(result) >= tc->plainLength);
        Esc_ASSERT(sizeof(result) >= tc->cipherLength);

        EscTst_PrintString( "Test vector " );
        EscTst_PrintWordOnly(i+1);
        EscTst_PrintString( ": " );

        /* Test wrap */

        returnCode = EscAesKeyWrap_wrapKey(tc->kek, tc->keySizeBits, tc->iv, tc->plain, result, tc->plainLength);

        if (returnCode == Esc_NO_ERROR)
        {
            isEqual = EscTst_Memcmp(result, tc->cipher, tc->cipherLength);
            if (isEqual == FALSE)
            {
                returnCode = Esc_KAT_FAILED;
            }
        }

        /* Test unwrap */

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscAesKeyWrap_unwrapKey(tc->kek, tc->keySizeBits, tc->iv, tc->cipher, result, tc->cipherLength);
        }

        if (returnCode == Esc_NO_ERROR)
        {
            isEqual = EscTst_Memcmp(result, tc->plain, tc->plainLength);
            if (isEqual == FALSE)
            {
                returnCode = Esc_KAT_FAILED;
            }
        }

        if (returnCode == Esc_NO_ERROR)
        {
            EscTst_PrintString( "correct\n" );
        }
        else
        {
            EscTst_PrintString( "failed\n" );
        }
    }

    return returnCode;
}

static Esc_ERROR
EscTstAesKeyWrap_NegativeTest(void)
{
    Esc_ERROR returnCode;
    Esc_UINT8 result[64];

    EscTst_PrintString( "Negative test\n" );

    returnCode = EscAesKeyWrap_unwrapKey(
            EscTstAesKeyWrap_rfc41_key,
            128U,
            Esc_NULL_PTR,
            EscTstAesKeyWrap_rfc41_cipherInvalid,
            result,
            sizeof(EscTstAesKeyWrap_rfc41_cipherInvalid));

    returnCode = EscTst_CheckResultFailed(&returnCode, Esc_IV_MISMATCH);

    return returnCode;
}

Esc_ERROR
EscTstAesKeyWrap_Perform(void)
{
    Esc_ERROR returnCode;

    EscTst_PrintString( "Test AES key wrapping\n" );
    EscTst_PrintString( "---------------------\n" );

    returnCode = EscTstAesKeyWrap_KeyWrapParams();

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstAesKeyWrap_KeyUnwrapParams();
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstAesKeyWrap_ComplianceTest();
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstAesKeyWrap_NegativeTest();
    }

    return returnCode;
}

