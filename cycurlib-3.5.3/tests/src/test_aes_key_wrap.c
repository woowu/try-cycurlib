static Esc_ERROR
EscTstAesKeyWrap_KeyWrapParams(void)
{
    Esc_ERROR returnCode;

    Esc_UINT8 kek[16] = {0};
    Esc_UINT8 input[24] = {0};
    Esc_UINT8 output[24 + 8];

    const Esc_UINT32 minInputLen = 16U;

    EscTst_PrintString( "Parameter test for EscAesKeyWrap_wrapKey()\n" );

    EscTst_PrintString( "kek == NULL\n" );
    returnCode = EscAesKeyWrap_wrapKey(Esc_NULL_PTR, 128U, Esc_NULL_PTR, input, output, sizeof(input));
    returnCode = EscTst_CheckResultFailed( &returnCode, Esc_NULL_POINTER_ERROR );

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "kekLen invalid\n" );
        returnCode = EscAesKeyWrap_wrapKey(kek, 127U, Esc_NULL_PTR, input, output, sizeof(input));
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_INVALID_KEY_LENGTH );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "kekLen larger than max. key size\n" );
        returnCode = EscAesKeyWrap_wrapKey(kek, (Esc_UINT16)(EscAes_MAX_KEY_BITS + 64U), Esc_NULL_PTR, input, output, sizeof(input));
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_INVALID_KEY_LENGTH );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "in == NULL\n" );
        returnCode = EscAesKeyWrap_wrapKey(kek, 128U, Esc_NULL_PTR, Esc_NULL_PTR, output, sizeof(input));
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_NULL_POINTER_ERROR );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "out == NULL\n" );
        returnCode = EscAesKeyWrap_wrapKey(kek, 128U, Esc_NULL_PTR, input, Esc_NULL_PTR, sizeof(input));
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_NULL_POINTER_ERROR );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "len less than minimum\n" );
        returnCode = EscAesKeyWrap_wrapKey(kek, 128U, Esc_NULL_PTR, input, output, minInputLen - 8U);
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_INVALID_PARAMETER );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "len not a multiple of 8\n" );
        returnCode = EscAesKeyWrap_wrapKey(kek, 128U, Esc_NULL_PTR, input, output, minInputLen - 1U);
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_INVALID_PARAMETER );
    }

    return returnCode;
}

