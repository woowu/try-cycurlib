Esc_ERROR
EscTstRsa_Perform(
    void )
{
    Esc_ERROR returnCode;

    EscTst_PrintWord( "EscTstRsa_KEY_BYTES_TEST", EscTstRsa_KEY_BYTES_TEST );
    EscTst_PrintString( "\n" );

    returnCode = EscTstRsa_Sig_Compliance();

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstRsa_KeyFromBytesParameters();
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstRsa_ModExpParameters();
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstRsa_KeyByteTest();
    }

#if EscRsa_GENRSA_ENABLED == 1
    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstRsa_KeyGeneration();
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstRsa_KeyGenParameters();
    }
#endif

#if EscRsa_CRT_ENABLED == 1
    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstRsa_Crt();
    }
#endif

    return returnCode;
}

