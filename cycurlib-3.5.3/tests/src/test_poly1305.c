static Esc_ERROR
EscTstPoly1305_CompliancePiece(
    const EscTstPoly1305_TestT* tst_vec )
{
    Esc_ERROR returnCode = Esc_NO_ERROR;
    Esc_UINT8 mac[EscPoly1305_MAX_MAC_LENGTH];
    EscPoly1305_ContextT ctx;
    Esc_UINT32 chunkSize;


    EscTst_PrintString( "\n Tests the vector piece by piece with different chunk lengths\n" );

    EscTst_PrintArray( "Key", tst_vec->key, EscPoly1305_KEY_BYTES );
    EscTst_PrintArray( "Plaintext", tst_vec->msg, tst_vec->msgLen );

    /* EscPoly1305_BLOCK_SIZE: 16U */
    for (chunkSize = 1U; (chunkSize <= 16U) && (returnCode == Esc_NO_ERROR); chunkSize++)
    {
        Esc_UINT32 remainingLen = tst_vec->msgLen;
        Esc_UINT32 currentLen;
        Esc_UINT32 offset = 0U;

        /* Init Poly1305 */
        returnCode = EscPoly1305_Init( &ctx, tst_vec->key );

        /* Call update() with zero length and Null Ptr first */
        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscPoly1305_Update( &ctx, Esc_NULL_PTR, 0U );
        }

        /* Call update() with given chunkSize until message is processed */
        while (remainingLen > 0U)
        {
            if (remainingLen > chunkSize)
            {
                currentLen = chunkSize;
            }
            else
            {
                currentLen = remainingLen;
            }

            if (returnCode == Esc_NO_ERROR)
            {
                returnCode = EscPoly1305_Update( &ctx, &tst_vec->msg[offset], currentLen );
            }

            remainingLen -= currentLen;
            offset += currentLen;
        }

        /* Call update() with zero length again */
        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscPoly1305_Update( &ctx, &tst_vec->msg[offset], 0U );
        }

        /* Finish the MAC */
        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscPoly1305_Finish( &ctx, mac, EscPoly1305_MAX_MAC_LENGTH );
        }

        /* Compare mac */
        if (returnCode == Esc_NO_ERROR)
        {
            EscTst_PrintArray( "Expected Mac", tst_vec->mac, EscPoly1305_MAX_MAC_LENGTH );
            EscTst_PrintArray( "Computed Mac", mac, EscPoly1305_MAX_MAC_LENGTH );

            if (EscTst_Memcmp( tst_vec->mac, mac, EscPoly1305_MAX_MAC_LENGTH ) == FALSE)
            {
                returnCode = Esc_KAT_FAILED;
            }
        }
    }

    return returnCode;
}


