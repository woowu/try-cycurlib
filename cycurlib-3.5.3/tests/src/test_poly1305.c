static Esc_ERROR
EscTstPoly130_TruncationTest_RFC7359(
    void )
{
    Esc_ERROR returnCode = Esc_NO_ERROR;
    Esc_UINT8 mac[EscPoly1305_MAX_MAC_LENGTH];
    Esc_UINT8 receivedMac[EscPoly1305_MAX_MAC_LENGTH];
    Esc_UINT8 zeroArray[EscPoly1305_MAX_MAC_LENGTH];
    EscPoly1305_ContextT ctx;
    Esc_UINT8 i;
    Esc_UINT8 macLength = (Esc_UINT8)(EscPoly1305_MAX_MAC_LENGTH/2U);

    EscTst_PrintString( "Poly1305 truncation test with macLength = EscPoly1305_MAX_MAC_LENGTH/2\n" );

    for (i = 0; i < EscTstPoly1305_RFC7359_TESTS_NUM; i++)
    {
#if EscTst_ENABLE_LOGGING == 1
        EscTst_PrintString( "Testcase: " );
        EscTst_PrintString( EscTstPoly1305_RFC7539_TESTS[i].title );
        EscTst_PrintString( "\n" );
#endif
        EscTst_Memset( mac, EscPoly1305_MAX_MAC_LENGTH, 0 );
        EscTst_Memset( receivedMac, EscPoly1305_MAX_MAC_LENGTH, 0 );
        EscTst_Memset( zeroArray, EscPoly1305_MAX_MAC_LENGTH, 0 );

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscPoly1305_Calc( EscTstPoly1305_RFC7539_TESTS[i].key, EscTstPoly1305_RFC7539_TESTS[i].msg,
                                      EscTstPoly1305_RFC7539_TESTS[i].msgLen, mac, EscPoly1305_MAX_MAC_LENGTH );
        }

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscPoly1305_Init( &ctx, EscTstPoly1305_RFC7539_TESTS[i].key );
        }
        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscPoly1305_Update( &ctx, EscTstPoly1305_RFC7539_TESTS[i].msg,
                                             EscTstPoly1305_RFC7539_TESTS[i].msgLen );
        }
        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscPoly1305_Finish( &ctx, receivedMac, macLength );
        }

        if (returnCode == Esc_NO_ERROR)
        {
            /* compare results */
            EscTst_PrintArray( "Expected result", mac, (Esc_UINT32)macLength );
            EscTst_PrintArray( "Received result", receivedMac, (Esc_UINT32)macLength );
            if ( EscTst_Memcmp( receivedMac, mac, (Esc_UINT32)macLength ) )
            {
                if ( EscTst_Memcmp( &receivedMac[macLength], &zeroArray[macLength], (Esc_UINT32)macLength ) )
                {
                    EscTst_PrintString( "Correct\n\n\n" );
                }
                else
                {
                    EscTst_PrintString( "FAILED truncation!!!\n\n\n" );
                    returnCode = Esc_KAT_FAILED;
                }
            }
            else
            {
                EscTst_PrintString( "FAILED!!!\n\n\n" );
                returnCode = Esc_KAT_FAILED;
            }
        }
    }

    return returnCode;
}


