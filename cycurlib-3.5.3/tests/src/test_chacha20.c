static Esc_ERROR
EscTstChaCha20_CompliancePiece(
    const EscTstsChaCha20_tst_vector* tst_vec )
{
    Esc_ERROR returnCode = Esc_NO_ERROR;

    Esc_UINT32 i;
    Esc_UINT8 ciphertext[EscTstChaCha20_MAX_PLAINTEXT_BYTES];
    EscChaCha20_ContextT ctx;
    Esc_UINT32 chunkSize;

    EscTst_PrintString( "\n Tests the vector piece by piece with different chunk lengths\n" );

    EscTst_PrintArray( "Key", tst_vec->key, (tst_vec->keyBits) / 8U );
    EscTst_PrintArray( "Nonce", tst_vec->nonce, EscChaCha20_NONCE_BYTES );
    EscTst_PrintWord( "Block Counter", tst_vec->blockCounter );
    EscTst_PrintArray( "Plaintext", tst_vec->plaintext, tst_vec->plaintextLen );

    for (chunkSize = 1U; (chunkSize <= EscChaCha20_BLOCK_BYTES) && (returnCode == Esc_NO_ERROR); chunkSize++)
    {
        Esc_UINT32 remainingLen = tst_vec->plaintextLen;
        Esc_UINT32 currentLen;
        Esc_UINT32 offset = 0U;

        /* zeroize cipher text array */
        for ( i = 0U; i < EscTstChaCha20_MAX_PLAINTEXT_BYTES; i++)
        {
            ciphertext[i] = 0U;
        }

        /* Init ChaCha20 */
        returnCode = EscChaCha20_Init( &ctx, tst_vec->keyBits, tst_vec->key, tst_vec->nonce, tst_vec->blockCounter );

        if (returnCode == Esc_NO_ERROR)
        {   /* Start encryption */
            returnCode = EscChaCha20_Start( &ctx );
        }

        /* Call update() with zero length first */
        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscChaCha20_Update( &ctx, &tst_vec->plaintext[offset], &ciphertext[offset], 0U );
        }

        /* Call update() with given chunkSize until message is processed */
        while (remainingLen > 0U)
        {
            if (remainingLen > chunkSize)
            {
                currentLen = chunkSize;
            }
            else
            {
                currentLen = remainingLen;
            }

            if (returnCode == Esc_NO_ERROR)
            {
                returnCode = EscChaCha20_Update( &ctx, &tst_vec->plaintext[offset], &ciphertext[offset], currentLen );
            }

            remainingLen -= currentLen;
            offset += currentLen;
        }

        /* Call update() with zero length again */
        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscChaCha20_Update( &ctx, &tst_vec->plaintext[offset], &ciphertext[offset], 0U );
        }

        /* Compare ciphertext */
        if (returnCode == Esc_NO_ERROR)
        {
            EscTst_PrintArray( "Expected Ciphertext", tst_vec->ciphertext, tst_vec->plaintextLen );
            EscTst_PrintArray( "Computed Ciphertext", ciphertext, tst_vec->plaintextLen );

            if (EscTst_Memcmp( tst_vec->ciphertext, ciphertext, tst_vec->plaintextLen ) == FALSE)
            {
                returnCode = Esc_KAT_FAILED;
            }
        }

        if (returnCode == Esc_NO_ERROR)
        {
            if (EscTst_MemIsZero( &ciphertext[tst_vec->plaintextLen], EscTstChaCha20_MAX_PLAINTEXT_BYTES - tst_vec->plaintextLen ) == FALSE)
            {
                EscTst_PrintString( "Remaining bytes are not zero!\n" );
                returnCode = Esc_KAT_FAILED;
            }
        }
    }

    return returnCode;
}

static Esc_ERROR
EscTstChaCha20_Compliance(
    const EscTstsChaCha20_tst_vector* tst_vec )
{
    Esc_ERROR returnCode;
    Esc_UINT32 i;
    Esc_UINT8 ciphertext[EscTstChaCha20_MAX_PLAINTEXT_BYTES];
    EscChaCha20_ContextT ctx;


    EscTst_PrintArray( "Key", tst_vec->key, (tst_vec->keyBits) / 8U );
    EscTst_PrintArray( "Nonce", tst_vec->nonce, EscChaCha20_NONCE_BYTES );
    EscTst_PrintWord( "Block Counter", tst_vec->blockCounter );
    EscTst_PrintArray( "Plaintext", tst_vec->plaintext, tst_vec->plaintextLen );

    /* Init ChaCha20 */
    returnCode = EscChaCha20_Init( &ctx, tst_vec->keyBits, tst_vec->key, tst_vec->nonce, tst_vec->blockCounter );

    if (returnCode == Esc_NO_ERROR)
    {   /* Start encryption */
        returnCode = EscChaCha20_Start( &ctx );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        /* Encrypt */
        returnCode = EscChaCha20_Update( &ctx, tst_vec->plaintext, ciphertext, tst_vec->plaintextLen );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintArray( "Expected Ciphertext", tst_vec->ciphertext, tst_vec->plaintextLen );
        EscTst_PrintArray( "Computed Ciphertext", ciphertext, tst_vec->plaintextLen );

        if (EscTst_Memcmp( tst_vec->ciphertext, ciphertext, tst_vec->plaintextLen ) == FALSE)
        {
            returnCode = Esc_KAT_FAILED;
        }
    }

    /* Reset block counter and generate same keystream */
    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "\nReset block counter and re-generate ciphertext\n" );
        for ( i = 0U; i < EscTstChaCha20_MAX_PLAINTEXT_BYTES; i++ )
        {
            ciphertext[ i ] = 0U;
        }
        returnCode = EscChaCha20_UpdateCnt( &ctx, tst_vec->blockCounter );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscChaCha20_Start( &ctx );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        /* Encrypt */
        returnCode = EscChaCha20_Update( &ctx, tst_vec->plaintext, ciphertext, tst_vec->plaintextLen );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintArray( "Expected Ciphertext", tst_vec->ciphertext, tst_vec->plaintextLen );
        EscTst_PrintArray( "Computed Ciphertext", ciphertext, tst_vec->plaintextLen );

        if (EscTst_Memcmp( tst_vec->ciphertext, ciphertext, tst_vec->plaintextLen ) == FALSE)
        {
            returnCode = Esc_KAT_FAILED;
        }
    }

    return returnCode;
}


Esc_ERROR
EscTstChaCha20_Perform(
    void )
{
    Esc_ERROR returnCode = Esc_NO_ERROR;
    Esc_UINT8 i;
    Esc_UINT16 numTests = (Esc_UINT16)(sizeof(EscChaCha20_allTestVectors) / sizeof(EscChaCha20_allTestVectors[0]));

    EscTst_PrintString( "\n\nChaCha20 Compliance test\n" );
    for ( i = 0U; (i < numTests) && (returnCode == Esc_NO_ERROR); i++) 
    {
        /* Compliance test */
        EscTst_PrintWord( "\nTest vector #", i+1U );
        returnCode = EscTstChaCha20_Compliance(&EscChaCha20_allTestVectors[i]);

        if ( (returnCode == Esc_NO_ERROR) && (EscChaCha20_allTestVectors[i].plaintextLen >= 11U) )
        {
            returnCode = EscTstChaCha20_CompliancePiece(&EscChaCha20_allTestVectors[i]);
        }
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstChaCha20_InitParams( &EscChaCha20_allTestVectors[0] );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstChaCha20_UpdateCntParams( &EscChaCha20_allTestVectors[0] );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstChaCha20_StartParams( );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstChaCha20_UpdateParams( &EscChaCha20_allTestVectors[0] );
    }


    return returnCode;
}

