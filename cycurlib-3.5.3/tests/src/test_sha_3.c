static Esc_ERROR
EscTstSha3_TruncationTest(
    void )
{
    const Esc_CHAR* msg = "abc";
    Esc_UINT8 result[EscSha3_512_DIGEST_LEN];
    Esc_UINT8 receivedDigest[EscSha3_512_DIGEST_LEN];
    Esc_UINT8 zeroArray[EscSha3_512_DIGEST_LEN];
    Esc_ERROR returnCode = Esc_NO_ERROR;
    EscSha3_ContextT ctx;
    Esc_UINT8 i, j;
    Esc_UINT8 digestLength;

    EscTst_PrintString( "SHA-3 truncation test with digestLength = EscSha3_XXX_DIGEST_LEN/2\n" );

    for (i = 0U; (i < 4U) && (returnCode == Esc_NO_ERROR); i++)
    {
        switch (i)
        {
            case EscSha3_224:
                digestLength = (Esc_UINT8) (EscSha3_224_DIGEST_LEN/2U);
                EscTst_PrintString( "SHA3-224 Truncation test\n" );
                break;

            case EscSha3_256:
                digestLength = (Esc_UINT8) (EscSha3_256_DIGEST_LEN/2U);
                EscTst_PrintString( "SHA3-256 Truncation test\n" );
                break;

            case EscSha3_384:
                digestLength = (Esc_UINT8) (EscSha3_384_DIGEST_LEN/2U);
                EscTst_PrintString( "SHA3-384 Truncation test\n" );
                break;

            case EscSha3_512:
                digestLength = (Esc_UINT8) (EscSha3_512_DIGEST_LEN/2U);
                EscTst_PrintString( "SHA3-512 Truncation test\n" );
                break;

            default:
                returnCode = Esc_INTERNAL_FUNCTION_ERROR;
                break;
        }

        if (returnCode == Esc_NO_ERROR)
        {
            for (j = 0; j < EscSha3_512_DIGEST_LEN; j++)
            {
                zeroArray[j] = 0U;
                receivedDigest[j] = 0U;
                result[j] = 0U;
            }
            /*lint -e{644} digestLength is initialized */
            returnCode = EscSha3_Calc(i, (const Esc_UINT8*)msg, EscTst_Strlen( msg ), result, (Esc_UINT8)(digestLength*2U) );
        }

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscSha3_Init(i, &ctx);
        }

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscSha3_Update( &ctx, (const Esc_UINT8*)msg, EscTst_Strlen( msg ) );
        }

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscSha3_Finish( &ctx, receivedDigest, digestLength);
        }

        if (returnCode == Esc_NO_ERROR)
        {
            /* compare results */
            EscTst_PrintArray( "Expected result", result, (Esc_UINT32)digestLength );
            EscTst_PrintArray( "Received result", receivedDigest, (Esc_UINT32)digestLength );
            if ( EscTst_Memcmp( receivedDigest, result, (Esc_UINT32)digestLength ) )
            {
                if ( EscTst_Memcmp( &receivedDigest[digestLength], &zeroArray[digestLength], (Esc_UINT32)digestLength ) )
                {
                    EscTst_PrintString( "Correct\n\n\n" );
                }
                else
                {
                    EscTst_PrintString( "FAILED truncation!!!\n\n\n" );
                    returnCode = Esc_KAT_FAILED;
                }
            }
            else
            {
                EscTst_PrintString( "FAILED!!!\n\n\n" );
                returnCode = Esc_KAT_FAILED;
            }
        }
    }

    return returnCode;
}

Esc_ERROR
EscTstSha3_Perform(
    void )
{
    Esc_ERROR returnCode;

    EscTst_PrintString( "SHA-3 Parameter Tests\n\n" );
    returnCode = EscTstSha3_InitParamTest();

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstSha3_UpdateParamTest();
    }
    
    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstSha3_FinishParamTest();
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstSha3_CalcParamTest();
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstSha3_StepTests();
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstSha3_ComplianceTest();
    }

    if ( returnCode == Esc_NO_ERROR )
    {
        returnCode = EscTstSha3_TruncationTest();
    }

    return returnCode;
}

