static Esc_ERROR
EscTstSha3_StepTests(
    void )
{
    Esc_ERROR returnCode = Esc_NO_ERROR;
    EscSha3_ContextT ctx;
    Esc_UINT8 digest[EscSha3_512_DIGEST_LEN];
    Esc_UINT32 chunkSize;
    const Esc_UINT8 *testMsg = EscTstSha3_Testcases[9].msg;
    const Esc_UINT8 *refDigest = EscTstSha3_Testcases[9].digest512;
    Esc_UINT32 testMsgLen = EscTstSha3_Testcases[9].length;

    EscTst_PrintString( "Sha3 testing multiple update calls\n" );

    for (chunkSize = 1U; (chunkSize <= 32U) && (returnCode == Esc_NO_ERROR); chunkSize++)
    {
        Esc_UINT32 remainingLen = testMsgLen;
        Esc_UINT32 currentLen;
        Esc_UINT32 offset = 0U;

        returnCode = EscSha3_Init(EscSha3_512, &ctx);

        /* Call update() with zero length first */

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscSha3_Update(&ctx, testMsg, 0U);
        }

        /* Call update() with given chunkSize until message is processed */

        while (remainingLen > 0U)
        {
            if (remainingLen > chunkSize)
            {
                currentLen = chunkSize;
            }
            else
            {
                currentLen = remainingLen;
            }

            if (returnCode == Esc_NO_ERROR)
            {
                returnCode = EscSha3_Update(&ctx, &testMsg[offset], currentLen);
            }

            remainingLen -= currentLen;
            offset += currentLen;
        }

        /* Call update() with zero length again */

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscSha3_Update(&ctx, testMsg, 0U);
        }

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscSha3_Finish(&ctx, digest, EscSha3_512_DIGEST_LEN);
        }

        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscTst_CheckStrings(
                refDigest,
                EscSha3_512_DIGEST_LEN,
                digest,
                EscSha3_512_DIGEST_LEN );
        }
    }

    return returnCode;
}

