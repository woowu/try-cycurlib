/**
 * Perform the selftest
 */
Esc_ERROR
EscTstAesXts_Perform(
    void )
{
    Esc_ERROR returnCode = Esc_NO_ERROR;
    Esc_UINT8 j;

    for (j = 0; j < EscTstAesXts_NUM_KEYSIZES; ++j)
    {
        EscTst_PrintWord( "Aes KEY BITS", keySizes[j] );
        EscTst_PrintString( "*******************************************************\n" );

        /* Check functions with invalid parameters */
        returnCode = EscTstAesXts_EncDecParams(j);

        /* Check functions for compliance with the given testvectors from the NIST */
        if (returnCode == Esc_NO_ERROR)
        {
            returnCode = EscTstAesXts_ComplianceTest(j);
        }
        if (returnCode != Esc_NO_ERROR)
        {
            break;
        }

        EscTst_PrintString( "*******************************************************\n" );
    }

    return returnCode;
}

