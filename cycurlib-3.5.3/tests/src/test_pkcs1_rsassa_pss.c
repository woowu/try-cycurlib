static Esc_ERROR
EscTstPkcs1RsaSsaPss_VerifyParams(
    void )
{
    Esc_ERROR returnCode;
    Esc_UINT8 signature[EscTstRsa_KEY_BYTES_TEST];
    Esc_UINT8 digest[EscPkcs1RsaSsaPss_MAX_DIGEST_LEN];
    Esc_UINT8 seed[EscTstRandom_RND_LENGTH];
    EscTstRandom_ContextT rndCtx;
    EscRsa_PubKeyT pubKey;
    Esc_UINT8 tstDigestType = digestSalts[0].digestType;
    Esc_UINT32 tstSaltLen   = digestSalts[0].saltLength;

    /*initialization of digest, signature*/
    EscTst_InitArray( digest, EscPkcs1RsaSsaPss_MAX_DIGEST_LEN );
    EscTst_InitArray( signature, EscTstRsa_KEY_BYTES_TEST );

    EscTst_PrintString( "EscPkcs1RsaSsaPss_Verify() parameter test\n" );
    returnCode = EscRsa_PubKeyFromBytes( &pubKey, EscTstRsa_KEY_BITS_TEST, modulus, pubExp );

    /* Init Random number generator with predefined seed - use this only for testing! */
    EscTstPkcs1RsaSsaPss_GetSeed( seed );
    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstRandom_Init( &rndCtx, seed );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "digest == 0\n" );
        returnCode = EscPkcs1RsaSsaPss_Verify( Esc_NULL_PTR, &pubKey, signature, EscTstRsa_KEY_BYTES_TEST, tstSaltLen, tstDigestType );
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_NULL_POINTER_ERROR );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "pubKey == 0\n" );
        returnCode = EscPkcs1RsaSsaPss_Verify( digest, Esc_NULL_PTR, signature, EscTstRsa_KEY_BYTES_TEST, tstSaltLen, tstDigestType );
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_NULL_POINTER_ERROR );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "signature == 0\n" );
        returnCode = EscPkcs1RsaSsaPss_Verify( digest, &pubKey, Esc_NULL_PTR, EscTstRsa_KEY_BYTES_TEST, tstSaltLen, tstDigestType );
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_NULL_POINTER_ERROR );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "signatureLen != key size\n" );
        returnCode = EscPkcs1RsaSsaPss_Verify( digest, &pubKey, signature, EscTstRsa_KEY_BYTES_TEST - 1U, tstSaltLen, tstDigestType );
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_INVALID_PARAMETER );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "saltLen == EscTstRsa_KEY_BYTES_TEST\n" );
        returnCode = EscPkcs1RsaSsaPss_Verify( digest, &pubKey, signature, EscTstRsa_KEY_BYTES_TEST, (Esc_UINT32)EscTstRsa_KEY_BYTES_TEST, tstDigestType );
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_INVALID_PARAMETER );
    }

    if (returnCode == Esc_NO_ERROR)
    {
        EscTst_PrintString( "digestType == 200 (not existing)\n" );
        returnCode = EscPkcs1RsaSsaPss_Verify( digest, &pubKey, signature, EscTstRsa_KEY_BYTES_TEST, tstSaltLen, 200U );
        returnCode = EscTst_CheckResultFailed( &returnCode, Esc_UNSUPPORTED_DIGEST_TYPE );
    }

    return returnCode;
}

Esc_ERROR
EscTstPkcs1RsaSsaPss_Perform(
    void )
{
    Esc_ERROR returnCode = EscTstPkcs1RsaSsaPss_SignParams();

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstPkcs1RsaSsaPss_VerifyParams();
    }

    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscTstPkcs1RsaSsaPss_PairwCons();
    }

    return returnCode;
}

