    EscTst_InitArray( digest, EscPkcs1RsaSsaPss_MAX_DIGEST_LEN);
    EscTst_InitArray( signature, EscTstRsa_KEY_BYTES_TEST);
#if EscRsa_CRT_ENABLED == 1
    EscTst_InitArray( signatureCrt, EscTstRsa_KEY_BYTES_TEST);
#endif

    EscTst_PrintString( "PKCS1 PSS Pairwise consistency test\n" );
    returnCode = EscRsa_PrivKeyFromBytes( &privKey, EscTstRsa_KEY_BITS_TEST, modulus, pubExp, privExp );
