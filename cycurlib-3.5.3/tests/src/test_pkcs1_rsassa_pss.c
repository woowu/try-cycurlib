    if (returnCode == Esc_NO_ERROR)
    {
        returnCode = EscRsa_PrivKeyCrtFromBytes( &privKeyCrt, EscTstRsa_KEY_BITS_TEST, modulus, pubExp, p, q, dmp1, dmq1, iqmp );
    }
